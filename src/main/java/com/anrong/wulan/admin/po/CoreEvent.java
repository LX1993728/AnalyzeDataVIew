package com.anrong.wulan.admin.po;

import java.io.Serializable;

/**
 * 事件表
 * @author ty
 *
 */
public class CoreEvent implements Serializable{
	
	private static final long serialVersionUID = 5253668656976973644L;

	private Integer id;
	
	private int eventNodeId;
	
	private int eventId;
	private String date;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	/*核心事件名称*/
	private String eventName;
	private String eventPath;
	private String eventNode;
	private int eventTrigger;
	private int reach;
	private int countUser;
	private int reachUser;
	private int pathNum;
	private int reachPathNum;

	/*事件完成量*/
	private Integer finishNumber;
	
	/*未完成事件量*/
	private Integer unFinishNumber;

	/*事件总量*/
	private Integer eventNumber;
	
	/*判断是否为核心事件*/
	private String type;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public int getReachPathNum() {
		return reachPathNum;
	}
	public void setReachPathNum(int reachPathNum) {
		this.reachPathNum = reachPathNum;
	}
	public int getPathNum() {
		return pathNum;
	}
	public void setPathNum(int pathNum) {
		this.pathNum = pathNum;
	}
	public int getReachUser() {
		return reachUser;
	}
	public void setReachUser(int reachUser) {
		this.reachUser = reachUser;
	}
	public int getEventNodeId() {
		return eventNodeId;
	}
	public void setEventNodeId(int eventNodeId) {
		this.eventNodeId = eventNodeId;
	}
	public int getEventId() {
		return eventId;
	}
	public void setEventId(int eventId) {
		this.eventId = eventId;
	}
	public String getEventName() {
		return eventName;
	}
	public void setEventName(String eventName) {
		this.eventName = eventName;
	}
	public String getEventPath() {
		return eventPath;
	}
	public void setEventPath(String eventPath) {
		this.eventPath = eventPath;
	}
	public String getEventNode() {
		return eventNode;
	}
	public void setEventNode(String eventNode) {
		this.eventNode = eventNode;
	}
	public int getEventTrigger() {
		return eventTrigger;
	}
	public void setEventTrigger(int eventTrigger) {
		this.eventTrigger = eventTrigger;
	}
	public int getReach() {
		return reach;
	}
	public void setReach(int reach) {
		this.reach = reach;
	}
	public int getCountUser() {
		return countUser;
	}
	public void setCountUser(int countUser) {
		this.countUser = countUser;
	}
	public Integer getFinishNumber() {
		return finishNumber;
	}
	public void setFinishNumber(Integer finishNumber) {
		this.finishNumber = finishNumber;
	}
	public Integer getEventNumber() {
		return eventNumber;
	}
	public void setEventNumber(Integer eventNumber) {
		this.eventNumber = eventNumber;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	public Integer getUnFinishNumber() {
		return unFinishNumber;
	}
	public void setUnFinishNumber(Integer unFinishNumber) {
		this.unFinishNumber = unFinishNumber;
	}
	@Override
	public String toString() {
		return "CoreEvent [id=" + id + ", eventNodeId=" + eventNodeId + ", eventId=" + eventId + ", eventName="
				+ eventName + ", eventPath=" + eventPath + ", eventNode=" + eventNode + ", eventTrigger=" + eventTrigger
				+ ", reach=" + reach + ", countUser=" + countUser + ", reachUser=" + reachUser + ", pathNum=" + pathNum
				+ ", reachPathNum=" + reachPathNum + ", finishNumber=" + finishNumber + ", eventNumber=" + eventNumber
				+ ", type=" + type + "]";
	}
	
}
