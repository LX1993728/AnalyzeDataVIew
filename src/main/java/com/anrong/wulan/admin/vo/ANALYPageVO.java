package com.anrong.wulan.admin.vo;

import java.util.Date;

import com.anrong.wulan.admin.po.AnalyzePage;

/**
 * 封装查询的页面Model 包装类
 * 
 * @author liuxun
 *
 */
public class ANALYPageVO extends AnalyzePage {
	// 用来封装分页设置数据
	private Integer p = 1; // 默认值设置为1 用来封装页码
	private Integer offset = 0; // 默认设置为0
	private Integer pageSize = 10; // 每页数量，默认设置为10

	private Date beginDate; // 从哪一天进行查询
	private Date endDate; // 截止到哪一天结束

	public Integer getP() {
		return p;
	}

	public void setP(Integer p) {
		this.p = p;
	}

	public Integer getOffset() {
		return offset;
	}

	public void setOffset(Integer offset) {
		this.offset = offset;
	}

	public Integer getPageSize() {
		return pageSize;
	}

	public void setPageSize(Integer pageSize) {
		this.pageSize = pageSize;
	}

	public Date getBeginDate() {
		return beginDate;
	}

	public void setBeginDate(Date beginDate) {
		this.beginDate = beginDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

}
